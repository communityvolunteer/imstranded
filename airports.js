function getCountryList() {
  const seen = new Set();
  AIRPORTS_RAW.forEach(a => seen.add(a[3]));
  return [...seen].map(code => ({ code, name: COUNTRY_NAMES[code] || code }))
    .sort((a, b) => a.name.localeCompare(b.name));
}

function getAirportsForCountry(countryCode) {
  return AIRPORTS_RAW
    .filter(a => a[3] === countryCode)
    .map(a => ({ iata: a[0], city: a[1], name: a[2], countryCode: a[3], lat: a[4], lng: a[5] }))
    .sort((a, b) => a.city.localeCompare(b.city));
}

function findAirport(iata) {
  const a = AIRPORTS_RAW.find(a => a[0] === iata);
  if (!a) return null;
  return { iata: a[0], city: a[1], name: a[2], countryCode: a[3], countryName: COUNTRY_NAMES[a[3]], lat: a[4], lng: a[5] };
}

function searchAirports(query, limit = 8) {
  if (!query || query.length < 2) return [];
  const q = query.toLowerCase();
  const results = [];
  for (const a of AIRPORTS_RAW) {
    if (results.length >= limit) break;
    const iata = a[0].toLowerCase();
    const city = a[1].toLowerCase();
    const name = a[2].toLowerCase();
    const country = (COUNTRY_NAMES[a[3]] || '').toLowerCase();
    if (iata === q || city.startsWith(q) || name.includes(q) || country.startsWith(q) || iata.startsWith(q)) {
      results.push({ iata: a[0], city: a[1], name: a[2], countryCode: a[3], countryName: COUNTRY_NAMES[a[3]], lat: a[4], lng: a[5] });
    }
  }
  return results;
} 